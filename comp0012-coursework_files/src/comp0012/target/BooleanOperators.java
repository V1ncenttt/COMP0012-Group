public class BooleanOperators{
    public void test_or(){
        boolean one = true;
        boolean two = false;
        boolean x = one | two;
    }
    public void test_xor(){
        boolean one = true;
        boolean two = false;
        boolean x = one ^ two;
    }
    public void test_and(){
        boolean one = true;
        boolean two = false;
        boolean x = one && two;
    }
}